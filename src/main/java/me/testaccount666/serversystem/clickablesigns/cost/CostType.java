package me.testaccount666.serversystem.clickablesigns.cost;

/**
 * Represents the type of cost for using a sign.
 */
public enum CostType {
    /**
     * No cost for using the sign.
     */
    NONE,

    /**
     * Cost in experience points.
     */
    EXP,

    /**
     * Cost in economy currency.
     */
    ECONOMY
}